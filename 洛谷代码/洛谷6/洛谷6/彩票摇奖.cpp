int check(int array1[7], int array2[7])
{
	int a = 0;
	for (int i = 0; i < 7; i++)
	{
		for (int j = 0; j < 7; j++)
		{
			if (array2[i] == array1[j])
			{
				a++;
			}
		}
	}
	return a;
}




