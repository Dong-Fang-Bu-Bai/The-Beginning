//# 斐波那契数列
//
//## 题目描述
//
//斐波那契数列是指这样的数列：数列的第一个和第二个数都为 $1$，接下来每个数都等于前面 $2$ 个数之和。
//
//给出一个正整数 $a$，要求斐波那契数列中第 $a$ 个数是多少。
//
//## 输入格式
//
//第 $1$ 行是测试数据的组数 $n$，后面跟着 $n$ 行输入。每组测试数据占 $1$ 行，包括一个正整数 $a$（$1 \le a \le 30$）。
//
//## 输出格式
//
//输出有 $n$ 行，每行输出对应一个输入。输出应是一个正整数，为斐波那契数列中第 $a$ 个数的大小。
//
//## 样例 #1
//
//### 样例输入 #1
//
//```
//4
//5
//2
//19
//1
//```
//
//### 样例输出 #1
//
//```
//5
//1
//4181
//1
//```

#include<iostream>
using namespace std;

int xiao(int a)
{
	int s1[30];
	for (int i = 0; i < 30; i++)
	{
		if (i == 0 || i == 1)
		{
			s1[i] = 1;
		}
		else
		{
			s1[i] = s1[i - 2] + s1[i - 1];
		}
	}
	
	return s1[a];

}

int main1()
{
	int n;
	cin >> n;
	

	for (int i = 0; i < n; i++)
	{
		int a;
		cin >> a;
		cout << xiao(a - 1) << endl;
	}
	
	



	system("pause");
	return 0;
}